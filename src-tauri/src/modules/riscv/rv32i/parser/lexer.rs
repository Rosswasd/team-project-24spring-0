impl RV32IOpToken {
    pub fn name(&self) -> String {
        Into::<&'static str>::into(self).replace("_", ".")
    }
}

impl From<RV32IOpToken> for RISCVOpToken {
    fn from(op: RV32IOpToken) -> RISCVOpToken {
        unsafe {
            RISCVOpToken {
                val: *std::mem::transmute::<&_, &u8>(&op),
                get_opd_set: |v| &OP_LIST[v as usize],
            }
        }
    }
}
